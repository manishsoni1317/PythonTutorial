myList = [1, 8, 7, 2, 21, 15]   # Creating a list
print(myList)   # Printing the list

L1 = myList.sort()   # Sorting the list
print(L1)   # Printing the sorted list
print(myList)   # Printing the sorted list

myList.reverse()   # Reversing the list
print(myList)   # Printing the reversed list

myList.append(100)   # Appending 100 to the list   
print(myList)   # Printing the appended list

myList.insert(2, 200)   # Inserting 200 at index 2
print(myList)   # Printing the inserted list    

myList.remove(21)   # Removing 21 from the list
print(myList)   # Printing the list after removing 21   

myList.pop(3)   # Removing the element at index 3
myList.pop(4)   # Removing the element at index 4
print(myList)   # Printing the list after removing the element at index 3   